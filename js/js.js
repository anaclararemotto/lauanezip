var currentJoke = null;
var count = 0;
var isAnswerRevealed = false;

function generateNewJoke() {
  var randomIndex = Math.floor(Math.random() * jokes.length);
  currentJoke = jokes[randomIndex];
  isAnswerRevealed = false;

  document.getElementById("jokeText").textContent = currentJoke.setup;
  document.getElementById("answerText").textContent = currentJoke.punchline;
  document.getElementById("answerText").classList.remove("show");
  document.getElementById("hiddenMessage").style.display = "block";
  document.getElementById("revealBtn").disabled = false;

  count++;
  document.getElementById("counter").textContent = "Piadas contadas: " + count;

  var sticker = document.getElementById("sticker");
  sticker.classList.add("rotating");
  setTimeout(function () {
    sticker.classList.remove("rotating");
  }, 500);

  changeStickerPosition();
}

function revealAnswer() {
  if (!currentJoke || isAnswerRevealed) return;

  isAnswerRevealed = true;
  document.getElementById("hiddenMessage").style.display = "none";
  document.getElementById("answerText").classList.add("show");
  document.getElementById("revealBtn").disabled = true;

  var sticker = document.getElementById("sticker");
  sticker.textContent = "😂";
  sticker.classList.add("rotating");
  setTimeout(function () {
    sticker.classList.remove("rotating");
  }, 500);
}

function changeStickerPosition() {
  var sticker = document.getElementById("sticker");
  sticker.textContent = "😄";
  sticker.style.top = "auto";
  sticker.style.bottom = "auto";
  sticker.style.left = "auto";
  sticker.style.right = "auto";
  sticker.style.transform = "";

  var positions = [
    { top: "-1.2rem", right: "-1.2rem", transform: "rotate(15deg)" },
    { top: "-1.2rem", left: "-1.2rem", transform: "rotate(-20deg)" },
    { bottom: "-1.2rem", right: "-1.2rem", transform: "rotate(45deg)" },
    { bottom: "-1.2rem", left: "-1.2rem", transform: "rotate(-40deg)" },
    { top: "50%", right: "-1rem", transform: "translateY(-50%) rotate(25deg)" },
    {
      top: "-1.2rem",
      right: "50%",
      transform: "translateX(50%) rotate(10deg)",
    },
  ];

  var randomPosition = positions[Math.floor(Math.random() * positions.length)];

  for (var prop in randomPosition) {
    sticker.style[prop] = randomPosition[prop];
  }
}

